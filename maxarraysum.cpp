// Complete the maxSubsetSum function below.
int maxSubsetSum(vector<int> arr) {
    int res = 0, curr = 0;
    vector<int> dp;
    for (int i = 0; i < arr.size(); i++)
    {
        if (i > 1 && dp[i - 2])
            curr = arr[i] + dp[i - 2];
        else
            curr = arr[i];
        res = max(res, curr);
        dp.push_back(res);
    }
    return res;
}

